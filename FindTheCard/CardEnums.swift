import Foundation

enum cardColor: String, CaseIterable {
    case blue = "blue_"
    case pink = "pink_"
    case yellow  = "yellow_"
}

enum cardLogo: String, CaseIterable {
    case bird = "bird_"
    case skull = "skull_"
    case heart = "heart_"
}

enum cardNumber: String, CaseIterable {
    case one = "1"
    case two = "2"
    case three = "3"
}

